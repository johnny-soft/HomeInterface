import { executeCommand, commandExists } from '../shell-executor'
import type { NetworkInterface, DNSConfig, ProxyHost } from '@/types'
import { readFile, writeFile } from 'fs/promises'
import { existsSync } from 'fs'

/**
 * Lists all network interfaces
 */
export async function listInterfaces(): Promise<NetworkInterface[]> {
  const result = await executeCommand('ip', ['-j', 'addr', 'show'])
  if (result.exitCode !== 0) {
    throw new Error(result.stderr || 'Failed to list interfaces')
  }
  
  try {
    const data = JSON.parse(result.stdout)
    const interfaces: NetworkInterface[] = []
    
    for (const iface of data) {
      const addresses = (iface.addr_info || []).map((addr: { local: string; prefixlen: number; family: string }) => ({
        address: addr.local,
        netmask: prefixToNetmask(addr.prefixlen, addr.family),
        family: addr.family === 'inet' ? 'inet' : 'inet6' as const,
      }))
      
      // Get interface stats
      const statsResult = await executeCommand('cat', [`/sys/class/net/${iface.ifname}/statistics/rx_bytes`])
      const rxBytes = parseInt(statsResult.stdout) || 0
      const txResult = await executeCommand('cat', [`/sys/class/net/${iface.ifname}/statistics/tx_bytes`])
      const txBytes = parseInt(txResult.stdout) || 0
      
      interfaces.push({
        name: iface.ifname,
        type: getInterfaceType(iface.ifname, iface.link_type),
        mac: iface.address || '',
        state: iface.operstate === 'UP' ? 'up' : 'down',
        mtu: iface.mtu || 1500,
        addresses,
        dhcp: false, // Would need to check netplan config
        stats: {
          rx: rxBytes,
          tx: txBytes,
          rxPackets: 0,
          txPackets: 0,
        },
      })
    }
    
    return interfaces
  } catch {
    return []
  }
}

/**
 * Gets a specific interface
 */
export async function getInterface(name: string): Promise<NetworkInterface | null> {
  const interfaces = await listInterfaces()
  return interfaces.find(i => i.name === name) || null
}

/**
 * Sets interface up or down
 */
export async function setInterfaceState(name: string, state: 'up' | 'down'): Promise<void> {
  const result = await executeCommand('ip', ['link', 'set', name, state], { sudo: true })
  if (result.exitCode !== 0) {
    throw new Error(result.stderr || `Failed to set interface ${state}`)
  }
}

/**
 * Configures interface with static IP
 */
export async function configureStaticIP(
  interfaceName: string,
  address: string,
  netmask: string,
  gateway?: string,
  dns?: string[]
): Promise<void> {
  // Generate netplan config
  const prefix = netmaskToPrefix(netmask)
  const config = {
    network: {
      version: 2,
      ethernets: {
        [interfaceName]: {
          addresses: [`${address}/${prefix}`],
          ...(gateway && { gateway4: gateway }),
          ...(dns && { nameservers: { addresses: dns } }),
        },
      },
    },
  }
  
  // Write to netplan
  const configPath = `/etc/netplan/99-homelab-${interfaceName}.yaml`
  await writeFile(configPath, `# Generated by HomeLab Manager\n${JSON.stringify(config, null, 2).replace(/"([^"]+)":/g, '$1:')}`)
  
  // Apply netplan
  const result = await executeCommand('netplan', ['apply'], { sudo: true })
  if (result.exitCode !== 0) {
    throw new Error(result.stderr || 'Failed to apply network configuration')
  }
}

